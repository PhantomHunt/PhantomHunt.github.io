		 <h3>SUMMARY</h3>
          
		  <p>As the book begins, Athena is dead. How she ended up that way creates the intrigue sustaining the book. 
		  The child, Sherine Khalil renames herself Athena after her uncle was discussing with her mother on how her real name
		  will betray her origins and something like Athena gave nothing away. As a child, she shows a strong religious vocation
		  and reports seeing angels and saints, which both impresses and worries her parents.</p>

			<p>She goes into a London University to pursue Engineering at the age of 19 but it's not what her heart wanted. One day 
			she just decides that she wanted to drop out of college, get married and have a baby. Here, the author mentions that this 
			might be due to the fact that she was abandoned herself and wants to give all that love she could to her child which she 
			didn't receive from her birth mother. Two years later, her marriage falls apart because they are facing too many problems 
			due to their young age and lack of money or mostly because he felt that she loved only the child and used him to get what 
			she wanted. A very interesting quote she uses is "From Ancient Greece on, the people who returned from battle were either 
			dead on their shields or stronger, despite or because of their scars. It's better that way: I've lived on a battlefield since 
			I was born, but I'm still alive and I don't need anyone to protect me." She grows into a woman in search of answers to many 
			questions that arise within a person. She had a life which many felt was content because she had a child of her own, money, and
			friends; everything but her mind was at peace, so she searches for the answer to the classical question of "Who am I?" through many
			experiences. In her quest, she opens her heart to intoxicating powers of mother and becomes a controversial spiritual leader in London. </p>    
    