		 <h3>SUMMARY</h3>
          
		  <p>The Alchemist follows the journey of an Andalusian shepherd boy named Santiago. Believing a recurring dream to be prophetic, 
		  he decides to travel to a Romani fortune-teller in a nearby town to discover its meaning. The woman interprets the dream as a
		  prophecy telling the boy that there is a treasure in the pyramids in Egypt.</p>

		<p>Early into his journey, he meets an old king, whose name was Melchizedek or the king of Salem, who tells him to sell his sheep
		to travel to Egypt and introduces the idea of a Personal Legend. Your Personal Legend "is what you have always wanted to accomplish.
		Everyone, when they are young, knows what their Personal Legend is."[6] He adds that "when you want something, all the universe conspires 
		in helping you to achieve it." This is the core theme of the book.</p>

		<p>Along the way, the boy meets an Englishman who has come in search of an Alchemist and continues his travels with him. They travel through
		the Sahara desert and during his journey, Santiago meets and falls in love with a beautiful Arabian woman named Fatima, who resides with her 
		clan near an oasis. He asks Fatima to marry him, but she says she will only marry him after he completes his journey and finds his treasures. 
		He is perplexed by this, but later learns that true love will not stop nor plead to sacrifice one's Personal Legend, and if it does, it is not 
		true love.</p>

		<p>The boy then encounters a wise alchemist who also teaches him about Personal Legends. He says that people want to find only the treasure of 
		their Personal Legends but not the Personal Legend itself. The boy feels unsure about himself as he listens to the alchemist's teachings. The 
		alchemist states, "Those who don't understand their Personal Legends will fail to comprehend their teachings". It is also stated that treasure 
		is more worthy than gold. The main theme recurs all through the novel, "When a person really desires something, all the universe conspires to help
		that person to realize his dream." </p>    