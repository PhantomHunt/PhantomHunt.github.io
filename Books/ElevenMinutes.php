		 <h3>SUMMARY</h3>
          
		  <p>Maria, a young girl from a remote village of Brazil, whose first encounters with love leave her heartbroken, goes to seek her fortune
		  in Switzerland. She works for a time in a nightclub but soon becomes dissatisfied and after a heated discussion with her manager one night,
		  she quits her job. She tries to become a model but is unsuccessful. Because she is running out of money, she accepts 1000 francs from an 
		  Arab man to spend the night with him. She then decides to become a prostitute and ends up in a brothel on Rue de Berne, the heart of Geneva's
		  red-light district.</p>

		<p>There she befriends Nyah who gives her advice on her "new profession" and after learning the tricks of the trade from Milan, the brothel owner, 
		she enters the job with her body and mind shutting all doors for love and keeps her heart open only for her diary. Quickly she becomes quite 
		successful and famous and her colleagues begin to envy her. Months pass and Maria grows into a professionally groomed prostitute who not only 
		relaxes her clients' mind, but also calms their soul by talking to them about their problems.</p>

		<p>Her world turns upside down when she meets Ralf, a young Swiss painter, who sees her "inner light". Maria falls in love with him immediately 
		and begins to experience what true love is (according to the author, it is a sense of being for someone without actually possessing him/her).
		Maria is now split between her sexual fantasies and true love for Ralf. Eventually she decides that it is time for her to leave Geneva with her 
		memory of Ralf, because she realizes that they are worlds apart. But before leaving, she decides to rekindle the dead sexual fire in Ralf and 
		learns from him about the nature of Sacred Sex, sex which is mingled with true love and which involves the giving up of one's soul for the 
		loved one.</p>

		<p>This book explores the sacred nature of sex. "Eleven minutes" describes the duration of sex. Also, it depicts two types of prostitution: 
		prostitution for money and sacred prostitution. There are also direct references to sadomasochism. The story is of Maria's journey to find what 
		true love is by letting her own life guide her. She enters a life that leads her down the path of sexual awakening and almost leads to her 
		self-destruction when she is introduced to all sides of sexual experience. When she has given up hope to find true love, she finds her true 
		"inner light" and her everlasting true love.</p>