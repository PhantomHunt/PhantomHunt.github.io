		 <h3>SUMMARY</h3>
          
		  <p>The story begins in 1986 when Coelho undertakes his initiation into the order Regnus Agnus Mundi (RAM), which he subsequently fails.
		  He is then told that he must embark on a pilgrimage along the Camino de Santiago to find the sword that is the symbol of his acceptance
		  into the ranks of RAM. He must do this to gain insight into the simplicity of life. The journey transforms him as he learns to understand 
		  the nature of truth through the simplicity of life.</p>

			<p>He begins his journey with a guide, also a member of RAM, who goes by the alias Petrus. During the journey Petrus shows him meditation
			exercises and introduces him to some of the more down-to-earth elements of Western mystical thought and philosophy, and teaches him about
			love and its forms: agape, philia and eros.</p>    
	