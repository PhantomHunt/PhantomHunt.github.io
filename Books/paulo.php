		<p>
		Paulo Coelho de Souza is a Brazilian lyricist and novelist. He is the recipient of numerous international awards, 
		amongst them the Crystal Award by the World Economic Forum. Early in 2017 he has been nominated by the Albert Einstein
		Foundation as one of the 100 leading visionaries of our time . His novel The Alchemist has been translated into 81 
		languages. According to The Washington Post, Paulo Coelho has sold an estimated 350 million books and is the all-time 
		bestselling Portuguese-language author.</p>
		
		<h3><b>CAREER</b></h3>
		
		<p>In 1982, Coelho published his first book, Hell Archives, which failed to make a substantial impact. In 1986 he contributed 
		to the Practical Manual of Vampirism, although he later tried to take it off the shelves since he considered it "of bad quality. 
		"After making the pilgrimage to Santiago de Compostela in 1986, Coelho wrote The Pilgrimage that was published in the year 1987.
		The following year, Coelho wrote The Alchemist and published it through a small Brazilian publishing house who made an initial 
		print run of 900 copies and decided not to reprint. He subsequently found a bigger publishing house, and with the publication
		of his next book Brida, The Alchemist took off. HarperCollins, the biggest publishing House in the United States, decided to publish
		the book in 1994. Then, it became first a Brazilian bestseller, later a world-wide phenomenon. The Alchemist has gone on to sell more
		than 83 million copies, becoming one of the best-selling books in history, and has been translated into 81 different languages, winning
		the Guinness World Record for most translated book by a living author.</p>
		
		<p>The Alchemist, easily known as his most successful book, is a story about a young shepherd who travels from his homeland in Spain to 
		the Egyptian desert in search of a treasure buried in the Pyramids.  No one knows what the treasure is, or if Santiago will be able to 
		surmount the obstacles along the way. But what starts out as a journey to find worldly goods turns into a discovery of the treasure found
		within. The book has inspired a devoted following around the world,	with Will Smith, Madonna, and others having declared it to be their 
		favorite novel, and Bill Clinton having been seen with a copy of the book. In 2014, Oprah Winfrey interviewed pop star Pharrell Williams,
		who stated that The Alchemist had changed his life. </p>
		
		<p>Since the publication of The Alchemist, Coelho has generally written one novel every two years including By the River Piedra I Sat Down 
		and Wept, The Fifth Mountain, Veronika Decides to Die, The Devil and Miss Prym, Eleven Minutes, Brida, The Valkyries, The Winner Stands Alone, 
		The Zahir, The Witch of Portobello, Aleph, Manuscript Found in Accra, Adultery and The Spy. While trying to overcome his procrastination of 
		launching his writing career, Coelho said, "If I see a white feather today, that is a sign that God is giving me that I have to write a 
		new book." Coelho found a white feather in the window of a shop, and began writing that day. </p>
		
		<p>In total, Coelho has published 30 books. Three of them – The Pilgrimage,The Valkyries and Aleph – are autobiographical, while the majority of
		the rest are fictional, although rooted in his life experiences.Others, like Maktub, The Manual of the Warrior of Light and Like the Flowing River,
		are collections of essays, newspaper columns, or selected teachings. In total, Coelho has sold more than 210 million books in over 170 countries
		worldwide (June 2015 sales figures), and his works have been translated into 81 languages. On 22 December 2016, Coelho was listed by UK-based
		company Richtopia at number 2 in the list of 200 Most Influential Authors in the World. </p>
		
		<p>During the late 1990's to early 2000, Paulo Coelho agreed to contribute as a writer in the largest Brazilian Newspaper in the United States, 
		Florida Review. He was invited by both publishers Rodrigo Lisboa Soares (also Publisher and owner of "Diario Popular") and Marco A. Laureti.
		His stories enriched the Brazilian community and helped fuel the circulation of "Florida Review". </p>
	