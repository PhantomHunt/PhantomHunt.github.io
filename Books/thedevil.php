		 <h3>SUMMARY</h3>
          
		  <p>For almost fifteen years, old Berta had spent everyday sitting outside, watching over the little village Viscos, talking with her 
		  deceased husband. She is waiting for the devil to come, as her husband predicted. One day a stranger appears with the intention of staying
		  one week in the village.</p>

		<p>In the woods he buries 11 bars of gold. On the way back he meets Chantal Prym, a young and beautiful barmaid, who is bored of the idyllic
		scenery and slow pace of life. Regularly she seduces tourists in the hope that one of them will prove to be her escape route. The stranger shows
		her the buried treasure and promises that it will belong to the villagers if they agree to kill someone.</p>

		<p>There is a ferocious battle within the young woman; a battle between her angel and her devil. She sees in the gold the ticket to finally
		escape. Still, something holds her back.</p>

		<p>After some days, she decides to tell what the stranger has proposed, trusting that they will refuse. The people's reaction, however, plants 
		the seed of doubt inside of Chantal. Now she fears for her own life. As an act of desperation, she plans to abandon Viscos with one of the 
		stranger's bars. Destiny sends a rogue wolf, which threatens Chantal's life. The stranger arrives, and both escape.</p>

		<p>Meanwhile the villagers assemble to choose their victim. The scapegoat they choose is Berta, since she is already old and serves no purpose in 
		the village. Before the villagers shoot a sedated Berta, Chantal convinces them that under no circumstances murder is justified; our conduct is a 
		matter of control and choice. In the end the stranger receives the answers to his questions through a brief conversation between St. Savin and Arab 
		Leader Abah.</p>    
		
 